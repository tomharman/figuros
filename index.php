      <h3>What</h3>
      <p>Figuros are custom generated lamps based on the distance between two people. A lover, friend, celebrity or restauranteur, whatever matters to you.</p>
      <p>We take the co-ordinates from the latest 20 Instagrams by you and someone you follow before time-sorting them to calculate how far you were when each moment happened. These distances generate a custom shape, which is then lasercut from wood, transcluscent perspex and thick colored card stock and mailed to you in a 12" x 12" mailer.</p>
      <p>Spikier forms typically result from closer proximity, whereas more uniform shapes are derived from greater distances.</p>
      <p class="note">Note: In order for this to work you (and your partner) need an Instagram account with "Add to your Photo Map" switched on for at least 1 of your recent 20 photos.</p>
      <h3>Why</h3>
      <p>Figuros is an exploration in incorporating user creation into how we consume physical products. By embedding life experiences into material possessions we can embed personal stories into the physical objects that surround us.</p>
      <p>We live in a post-industrial-revolution era with the potential for digital fabrication technologies to produce highly personal objects. This project explores the space between material consumption and minimalism. Consider this a proposal for consuming fewer physical possessions, but making the ones you do more personally meaningful.</p>
