      <h3>What</h3>
      <p>Figuros is not just a lamp. It’s your lamp. Based on your life. Each one is different because every relationship is – with your friends, lovers and favourite celebrities.</p>
      <p>It works through the Instagram accounts of you and someone you follow, creating a unique shape based on the locations of your last 20 photos*. The closer you are physically to your partner, the spikier pattern you’ll get and the further apart you are, the smoother. It’s that simple.</p>
      <p>The lamps are lasercut from wood, translucent Perspex and thick colored card then sent to you in a 12" x 12" mailer, ready to take pride of place in your home – and your heart.</p>
      <p class="note">*In order to work, both of you must have "Add to your Photo Map" switched on in at least one of your last 20 photos.</p>
      <h3>Why</h3>
      <p>Figuros is an exploration in incorporating user creation into how we consume physical products. By embedding life experiences into material possessions we can embed personal stories into the physical objects that surround us.</p>
      <p>We live in a post-industrial-revolution era with the potential for digital fabrication technologies to produce highly personal objects. This project explores the space between material consumption and minimalism. Consider this a proposal for consuming fewer physical possessions, but making the ones you do more personally meaningful.</p>
